const { contextBridge, ipcRenderer } = require("electron");

contextBridge.exposeInMainWorld("toolMaster", {
  listTools: () => ipcRenderer.invoke("tools:list"),
  launchTool: (toolId) => ipcRenderer.invoke("tools:launch", toolId),
  openToolFolder: (toolId) => ipcRenderer.invoke("tools:open-folder", toolId),
  usageReport: (query) => ipcRenderer.invoke("usage:report", query),
  quota: () => ipcRenderer.invoke("quota:get"),
  switchCodexAccount: (accountId) => ipcRenderer.invoke("accounts:switch-codex", accountId),
  skillsList: () => ipcRenderer.invoke("skills:list"),
  skillRead: (filePath) => ipcRenderer.invoke("skills:read", filePath),
  skillSave: (filePath, patch) => ipcRenderer.invoke("skills:save", filePath, patch),
  skillCreate: (payload) => ipcRenderer.invoke("skills:create", payload),
  skillDelete: (filePath) => ipcRenderer.invoke("skills:delete", filePath),
  skillToggle: (filePath, enabled) => ipcRenderer.invoke("skills:toggle", filePath, enabled),
  skillOpenFolder: (filePath) => ipcRenderer.invoke("skills:open-folder", filePath),
  skillListPresets: () => ipcRenderer.invoke("skills:list-presets"),
  skillInstallPreset: (source, presetId) => ipcRenderer.invoke("skills:install-preset", source, presetId),
  skillExportPresets: () => ipcRenderer.invoke("skills:export-presets"),
  skillImportPresets: (files) => ipcRenderer.invoke("skills:import-presets", files),
  sessionsList: (query) => ipcRenderer.invoke("sessions:list", query),
  sessionRead: (filePath) => ipcRenderer.invoke("sessions:read", filePath),
  sessionOpenFolder: (cwd) => ipcRenderer.invoke("sessions:open-folder", cwd),
  sessionContinue: (payload) => ipcRenderer.invoke("sessions:continue", payload),
  projectsList: () => ipcRenderer.invoke("projects:list"),
  projectReadConfig: (projectPath, key) => ipcRenderer.invoke("projects:read-config", projectPath, key),
  projectSaveConfig: (projectPath, key, content) => ipcRenderer.invoke("projects:save-config", projectPath, key, content),
  projectDeleteConfig: (projectPath, key) => ipcRenderer.invoke("projects:delete-config", projectPath, key),
  projectSyncConfig: (payload) => ipcRenderer.invoke("projects:sync-config", payload),
  projectOpenFolder: (projectPath) => ipcRenderer.invoke("projects:open-folder", projectPath),
  projectChooseRoot: () => ipcRenderer.invoke("projects:choose-root"),
  openMarkdown: () => ipcRenderer.invoke("markdown:open"),
  readMarkdown: (filePath) => ipcRenderer.invoke("markdown:read", filePath),
  mcpListClients: () => ipcRenderer.invoke("mcp:list-clients"),
  mcpListServers: (clientId) => ipcRenderer.invoke("mcp:list-servers", clientId),
  mcpReadClient: (clientId) => ipcRenderer.invoke("mcp:read-client", clientId),
  mcpToggleServer: (clientId, serverName, enabled) => ipcRenderer.invoke("mcp:toggle-server", clientId, serverName, enabled),
  mcpSaveServer: (clientId, serverData) => ipcRenderer.invoke("mcp:save-server", clientId, serverData),
  mcpRemoveServer: (clientId, serverName) => ipcRenderer.invoke("mcp:remove-server", clientId, serverName),
  mcpDuplicateServer: (sourceClientId, serverName, targetClientId) => ipcRenderer.invoke("mcp:duplicate-server", sourceClientId, serverName, targetClientId),
  mcpListPresets: () => ipcRenderer.invoke("mcp:list-presets"),
  mcpInstallPreset: (clientId, presetId, variables) => ipcRenderer.invoke("mcp:install-preset", clientId, presetId, variables),
  mcpExportPresets: () => ipcRenderer.invoke("mcp:export-presets"),
  mcpExportPreset: (presetId) => ipcRenderer.invoke("mcp:export-preset", presetId),
  mcpImportPresets: (raw) => ipcRenderer.invoke("mcp:import-presets", raw),
  windowAction: (action) => ipcRenderer.invoke("window:action", action),
});
